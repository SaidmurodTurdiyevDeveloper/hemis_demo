class UniversityEntity {
  final String code;
  final String name;
  final String apiUrl;
  final String studentUrl;
  final String employeeUrl;
  final String universityType;
  final String versionType;

  UniversityEntity({
    required this.code,
    required this.name,
    required this.apiUrl,
    required this.studentUrl,
    required this.employeeUrl,
    required this.universityType,
    required this.versionType,
  });
}
